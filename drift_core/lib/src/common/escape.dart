bool isKeyword(Set<String> keywords, String s) {
  return keywords.contains(s.toUpperCase());
}

final _notInKeyword = RegExp('[^A-Za-z_0-9]');

/// Escapes [s] by wrapping it in backticks if it's an sqlite keyword.
String escapeIfNeeded(Set<String> keywords, String s) {
  final inUpperCase = s.toUpperCase();
  final isKeyword = baseKeywords.contains(inUpperCase);

  if (isKeyword || _notInKeyword.hasMatch(s)) return '"$s"';
  return s;
}

String sqlStringLiteral(String value) {
  final escape = value.replaceAll("'", "''");

  return "'$escape'";
}
